xa = float(input('Введите x-координату начальной точки: '))
ya = float(input('Введите y-координату начальной точки: '))
xb = float(input('Введите x-координату конечной точки: '))
yb = float(input('Введите y-координату конечной точки: '))

xc = (xa+xb) / 2
yc = (ya+yb) / 2
print(f'Координаты середины между двумя точками: [{xc}, {yc}]')